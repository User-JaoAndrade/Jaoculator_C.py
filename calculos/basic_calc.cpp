#include "basic_calc.h"


// Função de soma
float sum(float a, float b) {
    return a + b;
}

// Função de subtração
float subtration(float a, float b){
    return a - b;
}

// Função de Multiplicação
float product(float a, float b){
    return a * b;
}

// Função de Divisão
float division(float a, float b){
    return a / b;
}
